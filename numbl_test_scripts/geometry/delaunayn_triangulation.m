% delaunayn: N-D Delaunay triangulation (supported for n = 2 and n = 3).
%
% Points are in general position, so the triangulation is unique and must
% match the reference exactly. Expected values verified against MATLAB R2025b.
% Compared in canonical form (vertices sorted per simplex, then rows sorted).

% ---- n = 2 ----
X2 = [0.12 0.34; 0.83 0.11; 0.45 0.88; 0.97 0.52; 0.31 0.07; ...
      0.68 0.95; 0.05 0.61; 0.59 0.29; 0.24 0.73; 0.91 0.40];

T2 = sortrows(sort(delaunayn(X2), 2));

expected2 = [ ...
   1    5    8; ...
   1    7    9; ...
   1    8    9; ...
   2    4   10; ...
   2    5    8; ...
   2    8   10; ...
   3    4    6; ...
   3    4    8; ...
   3    7    9; ...
   3    8    9; ...
   4    8   10];

assert(isequal(T2, expected2), 'n=2 triangulation does not match reference');

% ---- n = 3 ----
X3 = [0.12 0.34 0.51; 0.83 0.11 0.27; 0.45 0.88 0.66; 0.97 0.52 0.13; ...
      0.31 0.07 0.79; 0.68 0.95 0.38; 0.05 0.61 0.92; 0.59 0.29 0.06; ...
      0.24 0.73 0.44; 0.91 0.40 0.71];

T3 = sortrows(sort(delaunayn(X3), 2));

expected3 = [ ...
   1   2   5    8; ...
   1   2   5   10; ...
   1   2   8   10; ...
   1   3   5    7; ...
   1   3   5   10; ...
   1   3   7    9; ...
   1   3   9   10; ...
   1   8   9   10; ...
   2   4   8   10; ...
   3   5   7   10; ...
   3   6   9   10; ...
   4   6   8   10; ...
   6   8   9   10];

assert(isequal(T3, expected3), 'n=3 triangulation does not match reference');
assert(size(T3, 2) == 4, 'expected tetrahedra (4 columns)');

disp('SUCCESS')
