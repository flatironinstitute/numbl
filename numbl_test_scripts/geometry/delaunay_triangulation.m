% delaunay: Delaunay triangulation in 2-D and 3-D.
%
% Points are in general position (no cocircular/cospherical degeneracies), so
% the triangulation is unique and must match the reference exactly. Expected
% values were verified against MATLAB R2025b.
%
% Triangulations are compared in canonical form: vertices sorted within each
% cell, then rows sorted, removing meaningless ordering/winding differences.

% ---- 2-D ----
x = [0.12; 0.83; 0.45; 0.97; 0.31; 0.68; 0.05; 0.59; 0.24; 0.91; 0.40; 0.76];
y = [0.34; 0.11; 0.88; 0.52; 0.07; 0.95; 0.61; 0.29; 0.73; 0.40; 0.18; 0.66];

DT2 = sortrows(sort(delaunay(x, y), 2));

expected2 = [ ...
   1    5   11; ...
   1    7    9; ...
   1    8    9; ...
   1    8   11; ...
   2    4   10; ...
   2    5   11; ...
   2    8   10; ...
   2    8   11; ...
   3    6   12; ...
   3    7    9; ...
   3    9   12; ...
   4    6   12; ...
   4   10   12; ...
   8    9   12; ...
   8   10   12];

assert(isequal(DT2, expected2), '2-D triangulation does not match reference');

% delaunay(P) with a matrix argument must give the same result as delaunay(x,y)
DT2p = sortrows(sort(delaunay([x, y]), 2));
assert(isequal(DT2p, expected2), 'delaunay(P) disagrees with delaunay(x,y)');

% ---- 3-D ----
px = [0.12; 0.83; 0.45; 0.97; 0.31; 0.68; 0.05; 0.59; 0.24; 0.91];
py = [0.34; 0.11; 0.88; 0.52; 0.07; 0.95; 0.61; 0.29; 0.73; 0.40];
pz = [0.51; 0.27; 0.66; 0.13; 0.79; 0.38; 0.92; 0.06; 0.44; 0.71];

DT3 = sortrows(sort(delaunay(px, py, pz), 2));

expected3 = [ ...
   1   2   5    8; ...
   1   2   5   10; ...
   1   2   8   10; ...
   1   3   5    7; ...
   1   3   5   10; ...
   1   3   7    9; ...
   1   3   9   10; ...
   1   8   9   10; ...
   2   4   8   10; ...
   3   5   7   10; ...
   3   6   9   10; ...
   4   6   8   10; ...
   6   8   9   10];

assert(isequal(DT3, expected3), '3-D triangulation does not match reference');

% Every tetrahedron must reference 4 distinct vertices.
assert(size(DT3, 2) == 4, 'expected tetrahedra (4 columns)');

disp('SUCCESS')
