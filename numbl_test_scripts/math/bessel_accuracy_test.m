% Accuracy test for Bessel functions against MATLAB reference values.
tol = 1e-12;

% Test points spanning small, medium, large, and chunkie-relevant arguments
x = [0.01, 0.1, 0.5, 1.0, 1.5, 2.0, 3.0, 5.0, 7.5, 10.0, 15.0, 20.0, 25.0, 30.0, 50.0];

% ---- besselj order 0 ----
ref = [9.99975000156249516e-01, 9.97501562066040126e-01, 9.38469807240812970e-01, ...
       7.65197686557966605e-01, 5.11827671735918144e-01, 2.23890779141235619e-01, ...
      -2.60051954901933502e-01,-1.77596771314338348e-01, 2.66339657880378389e-01, ...
      -2.45935764451348293e-01,-1.42244728267807343e-02, 1.67024664340583162e-01, ...
       9.62667832759581121e-02,-8.63679835810402113e-02, 5.58123276692518017e-02];
for i = 1:length(x)
    assert(abs(besselj(0, x(i)) - ref(i)) / max(abs(ref(i)), 1e-300) < tol, ...
        sprintf('besselj(0, %g) failed', x(i)));
end

% ---- besselj order 1 ----
ref = [4.99993750026041846e-03, 4.99375260362419984e-02, 2.42268457674873899e-01, ...
